public class Persona {
    private String nombre;
    private int edad;
    private char sexo;
    private String apellido;

    public Persona(String nombre, int edad, char sexo, String apellido) {
        this.nombre = nombre;
        this.edad = edad;
        this.sexo = sexo;
        this.apellido = apellido;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public char getSexo() {
        return sexo;
    }

    public void setSexo(char sexo) {
        this.sexo = sexo;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    

    
    
}